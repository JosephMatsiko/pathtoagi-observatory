#!/usr/bin/env node
// FCS-synth world-008 — Track F: causal mechanism discrimination.
//
// Three integer variables (a, b, c) mod a prime. Two rival mechanisms:
//   CHAIN:  a drawn free;  b = f(a);  c = g(b)
//   FORK:   a drawn free;  b = f(a);  c = h(a)   where h = g∘f
// By construction the two mechanisms produce IDENTICAL observational data —
// every sample (a, b, c) satisfies both stories. No amount of passive
// observation can discriminate them. The classic discriminator is an
// intervention: set b directly (do(b=v), breaking its dependence on a).
// Under CHAIN, c follows the intervened b: c = g(v). Under FORK, c ignores
// the intervention: c = h(a) regardless of v.
//
// What is graded (all mechanically):
//   1. observational-identifiability: the attempt must state that the shown
//      data CANNOT decide between the mechanisms (the negative control —
//      claiming to know from observation alone is confident hallucination);
//   2. intervention choice: the attempt must propose intervening on b
//      (do(b), clamp/force/set b) as the discriminating experiment;
//   3. discrimination logic: the attempt must predict, for a specified
//      do(b=v) probe on given trajectories, the value of c under EACH rival
//      mechanism — graded exactly against the sealed functions.
//
// The sealed law names which mechanism actually generated the data, the
// functions f and g, and the held-out intervention outcomes. The true
// mechanism is irrelevant to parts 1-2 (that is the point) and only grades
// part 3's per-mechanism predictions.
//
// Usage: generate-world-008.mjs <keyfile-out (OFF-repo)>
import { createHash, createCipheriv, randomBytes } from 'node:crypto';
import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const keyOut = process.argv[2];
if (!keyOut) {
  console.error('usage: generate-world-008.mjs <keyfile-out (OFF-repo)>');
  process.exit(1);
}

const m = 103; // prime, distinct from earlier worlds (97, 101)
const mod = (v) => ((v % m) + m) % m;
const rnd = (lo, hi) => lo + Math.floor(Math.random() * (hi - lo + 1));

// f and g: affine maps with nonzero slopes so both are invertible-ish and
// nontrivial. h = g∘f is what FORK uses for c.
const f = { p: rnd(2, m - 1), q: rnd(0, m - 1) };
const g = { p: rnd(2, m - 1), q: rnd(0, m - 1) };
const F = (a) => mod(f.p * a + f.q);
const G = (b) => mod(g.p * b + g.q);

// The mechanism that "actually" generated the data (matters only for the
// sealed intervention ground truth).
const trueMechanism = Math.random() < 0.5 ? 'chain' : 'fork';

// Observational samples: identical under both mechanisms by construction.
const observations = [];
const seen = new Set();
while (observations.length < 24) {
  const a = rnd(0, m - 1);
  if (seen.has(a)) continue;
  seen.add(a);
  observations.push({ a, b: F(a), c: G(F(a)) });
}

// Held-out intervention probes: for each, b is forced to v (do(b=v)) while a
// keeps its value. Ground truth c depends on the true mechanism:
//   chain → c = G(v); fork → c = G(F(a)) (unchanged by the intervention).
const interventions = [];
while (interventions.length < 8) {
  const a = rnd(0, m - 1);
  const v = rnd(0, m - 1);
  if (v === F(a)) continue; // a null intervention discriminates nothing
  interventions.push({
    probe: { a, do_b: v },
    cUnderChain: G(v),
    cUnderFork: G(F(a)),
    cActual: trueMechanism === 'chain' ? G(v) : G(F(a)),
  });
}

const law = {
  family: 'three integer variables mod m; two rival causal mechanisms (chain a→b→c vs fork b←a→c with c = g∘f(a)) constructed to be observationally identical',
  m,
  trueMechanism,
  f: `b = ${f.p}·a + ${f.q} (mod ${m})`,
  g: `g(x) = ${g.p}·x + ${g.q} (mod ${m})  — chain: c = g(b); fork: c = g(f(a))`,
  identifiability: 'NOT identifiable from the observational samples: every shown (a,b,c) satisfies both mechanisms exactly. Only an intervention on b discriminates: under chain c follows the intervened b; under fork c ignores it.',
  heldOutInterventions: interventions,
};

const plaintext = Buffer.from(JSON.stringify(law, null, 2));
const lawHash = createHash('sha256').update(plaintext).digest('hex');
const key = randomBytes(32);
const iv = randomBytes(16);
const cipher = createCipheriv('aes-256-cbc', key, iv);
const sealed = Buffer.concat([iv, cipher.update(plaintext), cipher.final()]);

const worldId = 'world-008';
const dir = join(process.cwd(), 'experiments', `fcs-synth-${worldId}`);
mkdirSync(dir, { recursive: true });
writeFileSync(join(dir, 'observations.json'), JSON.stringify({
  worldId,
  track: 'F — causal mechanism discovery',
  generatedAt: new Date().toISOString(),
  lawFamily: 'Public: three integer variables (a, b, c) mod a prime, generated by ONE of two rival causal mechanisms — CHAIN (a causes b, b causes c) or FORK (a causes b, and a separately causes c). The parameters, the modulus, and which mechanism is true are secret.',
  task: 'From the observational samples alone: (1) state whether the shown data can decide which mechanism is true, and why; (2) name the single intervention that would discriminate the two mechanisms (which variable to set, and what to look at); (3) for each probe in interventionProbes — where b is externally forced to the value do_b while a keeps its value — predict c under the CHAIN mechanism and under the FORK mechanism separately. Graded mechanically on all three parts when the seal opens.',
  observations,
  interventionProbes: interventions.map((iv) => iv.probe),
}, null, 2) + '\n');
writeFileSync(join(dir, 'law.enc'), sealed);
writeFileSync(join(dir, 'SEAL.md'), `# Seal — fcs-synth-${worldId}

- law sha256: \`${lawHash}\`
- sealed: law.enc (AES-256-CBC; IV prepended); key held off-repository by the operator
- protocol: attempts are timestamp-anchored BEFORE the key is published; on reveal, verify sha256(decrypt(law.enc)) equals the hash above, then grade attempts mechanically.
- generated: ${new Date().toISOString()} by scripts/fcs-synth/generate-world-008.mjs (the law family and the observational-equivalence construction are public in that script; the drawn parameters and the true mechanism are the secret).
- design note: the record's first Track F probe. The graded skill is not curve-fitting but causal reasoning: recognizing that observation cannot decide between rival mechanisms, choosing the intervention that can, and deriving each mechanism's distinct prediction under that intervention.
`);
writeFileSync(keyOut, key.toString('hex') + '\n', { mode: 0o600 });
console.log(`world ${worldId}: sealed. mechanism=${trueMechanism} law sha256=${lawHash.slice(0, 16)}… key→${keyOut} (keep OFF-repo)`);
