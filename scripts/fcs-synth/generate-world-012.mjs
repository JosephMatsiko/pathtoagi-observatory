#!/usr/bin/env node
// FCS-synth world-012 — REPLICATION of the wrong-frame attractor (world-003's
// failure mode) in the modern interventional format. The record's single
// evidence-against rests on n=1; this seal tests whether that failure
// replicates or was world-specific.
//
// The trap: y looks affine in x across most of the shown range, but the true
// law is GATED — a different affine branch above a hidden threshold. The shown
// observations include only a few above-threshold rows (the tell, in plain
// sight); the held-out do() probes concentrate where the branches diverge. A
// solver that locks the affine frame fits the majority and mispredicts the
// gate; one that notices the tell constructs the piecewise frame and passes.
//
// Usage: generate-world-012.mjs <keyfile-out (OFF-repo)>
import { createHash, createCipheriv, randomBytes } from 'node:crypto';
import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const keyOut = process.argv[2];
if (!keyOut) { console.error('usage: generate-world-012.mjs <keyfile-out>'); process.exit(1); }

const m = 127; // fresh prime
const mod = (v) => ((v % m) + m) % m;
const rnd = (lo, hi) => lo + Math.floor(Math.random() * (hi - lo + 1));

const T = rnd(80, 100);                 // hidden threshold
const A = { k: rnd(2, 40), c: rnd(0, m - 1) };   // affine branch (the attractor)
const B = { k: rnd(60, m - 1), c: rnd(0, m - 1) }; // divergent branch above T
const G = { k: rnd(2, m - 1), c: rnd(0, m - 1) };  // z = G(y), the observed edge
const fy = (x) => (x < T ? mod(A.k * x + A.c) : mod(B.k * x + B.c));
const fz = (y) => mod(G.k * y + G.c);
const obsFromX = (x) => ({ x, y: fy(x), z: fz(fy(x)) });
const intervene = (x, v, val) => {
  const out = obsFromX(x);
  out[v] = val;
  if (v === 'y') out.z = fz(val);  // y's only child is z
  // do(x) recomputes downstream from the forced x
  if (v === 'x') { out.y = fy(val); out.z = fz(out.y); }
  return out;
};

// Observations: mostly below the threshold (the attractor basin), with a few
// above-threshold rows — the tell is present, not hidden.
const observations = [];
const seenX = new Set();
while (observations.length < 18) {
  const x = rnd(0, T - 1);
  if (seenX.has(x)) continue; seenX.add(x);
  observations.push(obsFromX(x));
}
for (let i = 0; i < 4; i++) {
  const x = rnd(T, m - 1);
  if (seenX.has(x)) { i--; continue; } seenX.add(x);
  observations.push(obsFromX(x));
}

// Shown interventions: establish the causal shape (x->y->z), all below T.
const VARS = ['x', 'y', 'z'];
const shownInterventions = [];
while (shownInterventions.length < 6) {
  const x = rnd(0, T - 1), v = VARS[shownInterventions.length % 3], val = rnd(0, T - 1);
  shownInterventions.push({ do: { var: v, value: val }, before: obsFromX(x), after: intervene(x, v, val) });
}

// Held-out probes: concentrated across the divergence — do(x) above AND below
// the threshold, plus y/z probes. Frame-lock fails exactly here.
const heldOut = [];
while (heldOut.length < 12) {
  const x = rnd(0, m - 1);
  const v = VARS[rnd(0, 2)];
  const val = v === 'x' ? (heldOut.length % 2 === 0 ? rnd(T, m - 1) : rnd(0, T - 1)) : rnd(0, m - 1);
  heldOut.push({ before: obsFromX(x), do: { var: v, value: val }, after: intervene(x, v, val) });
}

const law = {
  family: 'REPLICATION of the wrong-frame attractor (world-003 class) in interventional format: y is GATED piecewise-affine in x with a hidden threshold; the shown range is dominated by one branch (the attractor); z = g(y).',
  m,
  threshold: T,
  trueStructure: `x exogenous; y = ${A.k}x+${A.c} if x<${T} else ${B.k}x+${B.c} (mod ${m}); z = ${G.k}y+${G.c} (mod ${m}).`,
  trap: 'The affine frame fits the below-threshold majority of the shown data. Four above-threshold observation rows contradict it in plain sight. Held-out do(x) probes straddle the threshold; an affine-locked solver mispredicts every above-threshold probe.',
  heldOut,
};

const plaintext = Buffer.from(JSON.stringify(law, null, 2));
const lawHash = createHash('sha256').update(plaintext).digest('hex');
const key = randomBytes(32), iv = randomBytes(16);
const cipher = createCipheriv('aes-256-cbc', key, iv);
const sealed = Buffer.concat([iv, cipher.update(plaintext), cipher.final()]);

const worldId = 'world-012';
const dir = join(process.cwd(), 'experiments', `fcs-synth-${worldId}`);
mkdirSync(dir, { recursive: true });
writeFileSync(join(dir, 'observations.json'), JSON.stringify({
  worldId,
  track: 'A/F — wrong-frame attractor replication, interventional format',
  generatedAt: new Date().toISOString(),
  lawFamily: 'UNDISCLOSED. Three integer variables (x, y, z) mod an unknown prime, generated by an unknown structure with unknown functions. The functional form is part of the secret.',
  task: 'From the observational samples and the shown interventional records: (1) construct the causal structure and the functions — including their FORM, which is not disclosed; (2) state them explicitly; (3) for each held-out probe (do_var forced to do_value on the disclosed `before` state), predict the exact post-intervention values of all three observed variables. Graded mechanically on held-out interventional predictions.',
  observations,
  shownInterventions,
  heldOutInterventionProbes: heldOut.map((h) => ({ before: h.before, do_var: h.do.var, do_value: h.do.value })),
}, null, 2) + '\n');
writeFileSync(join(dir, 'law.enc'), sealed);
writeFileSync(join(dir, 'SEAL.md'), `# Seal — fcs-synth-${worldId}

- law sha256: \`${lawHash}\`
- sealed: law.enc (AES-256-CBC; IV prepended); key held off-repository
- protocol: attempts anchored BEFORE the key is published; on reveal verify sha256(decrypt(law.enc)) equals the hash above, then grade held-out interventional predictions mechanically.
- generated: ${new Date().toISOString()} by scripts/fcs-synth/generate-world-012.mjs.
- design: replication of the wrong-frame-attractor failure mode (world-003, the record's first evidence-against, n=1) in the modern interventional format. The tell is in the shown data, in plain sight.
- status: LIVE — attempt per /MACHINE_PROTOCOL.md. Window closes 2026-07-23 (pre-registered).
`);
writeFileSync(keyOut, key.toString('hex') + '\n', { mode: 0o600 });
console.log(`world ${worldId}: sealed. law sha256=${lawHash.slice(0, 16)}… key→${keyOut}`);
