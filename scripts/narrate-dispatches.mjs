#!/usr/bin/env node
// Audio editions of the Press — the record gains a voice, generated locally
// by the instrument itself (macOS `say` → AAC via afconvert; zero external
// services, zero spend). Honest labeling: this is synthetic narration.
// Idempotent: re-narrates only when a dispatch's text changes. Prefers a
// premium voice when the operator has one installed; falls back to Samantha.
import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync, mkdirSync, existsSync, statSync, unlinkSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { tmpdir } from 'node:os';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'public', 'press', 'audio');
const MANIFEST = join(ROOT, 'src', 'data', 'audio-manifest.json');
mkdirSync(OUT, { recursive: true });

const dispatches = JSON.parse(readFileSync(join(ROOT, 'src', 'data', 'dispatches.json'), 'utf8'));
let manifest = [];
try { manifest = JSON.parse(readFileSync(MANIFEST, 'utf8')); } catch {}

// Prefer the best installed voice; Samantha is on every Mac.
const voices = execFileSync('say', ['-v', '?']).toString();
const pick = ['Ava (Premium)', 'Zoe (Premium)', 'Evan (Enhanced)', 'Samantha'];
const voice = pick.find((v) => voices.includes(v.split(' ')[0])) ?? 'Samantha';

for (const d of dispatches) {
  const text = [
    `The Observatory Press. Dispatch number ${d.no}. ${d.date}.`,
    d.title + '.',
    d.standfirst,
    ...d.body,
    `End of dispatch ${d.no}. This narration was generated by the instrument itself; the written record at pathtoagi-observatory.netlify.app is canonical.`,
  ].join('\n\n');
  const textHash = createHash('sha256').update(text).digest('hex').slice(0, 16);
  const file = `${d.slug}.m4a`;
  const existing = manifest.find((m) => m.slug === d.slug);
  if (existing?.textHash === textHash && existsSync(join(OUT, file))) {
    console.log(`= ${d.slug} (unchanged)`);
    continue;
  }
  const aiff = join(tmpdir(), `${d.slug}.aiff`);
  execFileSync('say', ['-v', voice, '-o', aiff, text]);
  execFileSync('afconvert', ['-f', 'm4af', '-d', 'aac', aiff, join(OUT, file)]);
  unlinkSync(aiff);
  const bytes = statSync(join(OUT, file)).size;
  const entry = { slug: d.slug, file: `/press/audio/${file}`, bytes, voice, textHash, generatedAt: new Date().toISOString() };
  manifest = manifest.filter((m) => m.slug !== d.slug);
  manifest.push(entry);
  console.log(`✓ narrated ${d.slug} (${voice}, ${(bytes / 1024).toFixed(0)} KB)`);
}
writeFileSync(MANIFEST, JSON.stringify(manifest, null, 2) + '\n');
